import UIKit

class AppColor: NSObject {
    
    class var warmBlack: UIColor {
        return UIColor.colorFromHexa("606060")
    }
    
    class var appOrange: UIColor {
        return UIColor.colorFromHexa("FE7C60")
    }
    
    class var lightGray: UIColor {
        return UIColor.colorFromHexa("C7C7CD")
    }
    
    class var creamWhite: UIColor {
        return UIColor.colorFromHexa("F8F8F8")
    }
}
